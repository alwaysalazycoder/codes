// methods in array...

let days = ["sun","mon","tue","wed","thurs","fri","sat"];

// 1. push method --> used to add an element at the end of the array...
days.push("newday");
console.log(days);

// 2. pop method --> used to remove an element form the end of the array...
days.pop();
console.log(days);

// 3. shift method --> shift remove the element from the array from the start...
days.shift();
console.log(days);

// 4. unshift method --> unshift adds an element to the start of the array....
days.unshift("newly added item");
console.log(days);

// 5. length method --> gives the length of the array or the no. of the elements in the array...
let length = days.length;
console.log(length);

